package BusinessEntity;

public class Categoria {
    
    private String cat_codigo;
    private String cat_descri;
    private String cat_estcod;
    
    public Categoria() {
    }

    public String getCat_codigo() {
        return cat_codigo;
    }

    public void setCat_codigo(String cat_codigo) {
        this.cat_codigo = cat_codigo;
    }

    public String getCat_descri() {
        return cat_descri;
    }

    public void setCat_descri(String cat_descri) {
        this.cat_descri = cat_descri;
    }

    public String getCat_estcod() {
        return cat_estcod;
    }

    public void setCat_estcod(String cat_estcod) {
        this.cat_estcod = cat_estcod;
    }

    
    
    
}
