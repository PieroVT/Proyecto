package BusinessEntity;

public class Cliente {
    private String cli_codigo;
    private String cli_docnum;
    private String cli_razsoc;
    private String cli_direcc;
    private String cli_email;
    private String cli_estcod;
   
    public Cliente() {
    }    

    public String getCli_codigo() {
        return cli_codigo;
    }

    public void setCli_codigo(String cli_codigo) {
        this.cli_codigo = cli_codigo;
    }

    public String getCli_docnum() {
        return cli_docnum;
    }

    public void setCli_docnum(String cli_docnum) {
        this.cli_docnum = cli_docnum;
    }

    public String getCli_razsoc() {
        return cli_razsoc;
    }

    public void setCli_razsoc(String cli_razsoc) {
        this.cli_razsoc = cli_razsoc;
    }

    public String getCli_direcc() {
        return cli_direcc;
    }

    public void setCli_direcc(String cli_direcc) {
        this.cli_direcc = cli_direcc;
    }

    public String getCli_email() {
        return cli_email;
    }

    public void setCli_email(String cli_email) {
        this.cli_email = cli_email;
    }

    public String getCli_estcod() {
        return cli_estcod;
    }

    public void setCli_estcod(String cli_estcod) {
        this.cli_estcod = cli_estcod;
    }

    
   
   
    
    
}
